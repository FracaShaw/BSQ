int	ft_atoi_n(char *str, int n)
{
	int	res;
	int	pos;

	res = 0;
	pos = 0;
	while (*str >= '0' && *str <= '9' && pos < n)
	{
		res = *str - '0' + res * 10;
		str++;
		pos++;
	}
	if (pos != n)
		return (0);
	return (res);
}
