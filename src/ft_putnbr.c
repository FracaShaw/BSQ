#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	div;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	div = nb % 10 + '0';
	write (1, &div, 1);
	return ;
}
